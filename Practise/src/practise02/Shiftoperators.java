package practise02;

public class Shiftoperators {
	public static void main(String[] args) {
		int a = 15;
		int b = -15;
		System.out.println("The left shift operator a<<2:"+(a<<2));
		System.out.println("The right shift operator a>>2:"+(a>>2));
		System.out.println("Unsigned right shift operator a>>>2:"+(a>>>2));
		System.out.println("Unsigned right shift operator b>>>2:"+(b>>>2));		
	
	}

}
